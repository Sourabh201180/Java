class A {
    public static void main(String[] args) {
        System.out.println(Boolean.toString(true));
        System.out.println(Character.toString('A'));
        byte a = 25;
        System.out.println(Byte.toString(a));
        short b = 257;
        System.out.println(Short.toString(b));
        System.out.println(Integer.toString(2453456));
        System.out.println(Long.toString(2453456847875L));
        System.out.println(Float.toString(2.4f));
        System.out.println(Double.toString(2.564));
    }
}